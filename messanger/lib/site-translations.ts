const translationMaps: Record<string, SiteTranslationMap> = {
  en,
  ru,
  es,
  fr,
  de,
  'zh-CN': zhCN,
  ja,
};

const missingLog = new Set<string>();

function interpolate(value: string, params: Record<string, string | number> | undefined) {
  if (!params) {
    return value;
  }

  return value.replace(/\{(\w+)\}/g, (_, key) => {
    const next = params[key];
    return typeof next === 'undefined' ? `{${key}}` : String(next);
  });
}

export function getSiteMessage(
  locale: SiteLocale,
  key: string,
  params?: Record<string, string | number>,
): string {
  const localized = translationMaps[locale]?.[key];
  const fallback = translationMaps[defaultSiteLocale]?.[key];
  const value = localized ?? fallback;

  if (!value) {
    if (process.env.NODE_ENV === 'development') {
      const token = `${locale}:${key}`;
      if (!missingLog.has(token)) {
        missingLog.add(token);
        console.warn(`[site-i18n] missing translation key: ${key} (locale ${locale})`);
      }
    }
    return key;
  }

  if (!localized && locale !== defaultSiteLocale && process.env.NODE_ENV === 'development') {
    const token = `${locale}:${key}:fallback`;
    if (!missingLog.has(token)) {
      missingLog.add(token);
      console.warn(`[site-i18n] fallback to en for key: ${key} (locale ${locale})`);
    }
  }

  return interpolate(value, params);
}

export function createSiteTranslator(locale: SiteLocale) {
  return (key: string, params?: Record<string, string | number>) =>
    getSiteMessage(locale, key, params);
}

export function getTranslationCoverage() {
  return siteLocales.filter((locale) => Boolean(translationMaps[locale]));
}
